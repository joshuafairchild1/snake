//------------------------------------------------------------------------------
//----------start the game------------------------------------------------------
//------------------------------------------------------------------------------
function game() {

  let direction = 'right';
  let score = 0;
  let snake = [];
  let gameOver = false;
  xPos += 10;
  moveSnake(direction, xPos, yPos);

}



//------------------------------------------------------------------------------
//----------user interface logic------------------------------------------------
//------------------------------------------------------------------------------
$(function() {
  setInterval(function() {
    game();
  }, frameRate);
});
